package com.unloadbrain.games.rockpaperscissors.core;

/**
 * The enum Game state.
 */
public enum GameState {
    /**
     * Ready to play game state.
     */
    READY_TO_PLAY, /**
     * Waiting for move game state.
     */
    WAITING_FOR_MOVE, /**
     * Finished game state.
     */
    FINISHED;
}